# Напишите программу, которая запрашивает у пользователя три числа и проверяет,
# делится ли первое число на сумму двух остальных чисел без остатка. Используйте оператор % для проверки кратности.
# Если первое число делится на сумму двух других чисел без остатка, выведите
# сообщение "Первое число делится на сумму двух других чисел", в противном случае - выведите "Первое
# число не делится на сумму двух других чисел".

a = int(input("Input 1 number: "))
b = int(input("Input 2 number: "))
c = int(input("Input 3 number: "))
if a % (b+c) == 0:
    print("1 number divisible by sum of number 2 and 3")
else:
    print("1 number isn't divisible by sum of number 2 and 3")





