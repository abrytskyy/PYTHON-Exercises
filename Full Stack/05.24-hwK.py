# 3. Определение возрастной группы:
# Напишите программу, которая запрашивает у пользователя его возраст и
# выводит сообщение о его возрастной группе: "Детство" (0-12 лет),
# "Юность" (13-17 лет), "Взрослый" (18-64 года) или "Пожилой" (65+ лет).
age = int(input("What is your age ?: "))
if age <= 12:
    print("Childhood")
elif age <= 17:
    print("Youth")
elif age <= 64:
    print("Adult")
else:
    print("Elderly")



# 2. Определение сезона:
# Напишите программу, которая запрашивает у пользователя номер месяца (от 1 до 12) и выводит название
# соответствующего сезона: "Зима", "Весна", "Лето" или "Осень".
number = int(input("Print number of the month: "))
if number in (1, 2, 12):
    print("winter")
elif number <= 5:
    print("spring")
elif number <= 8:
    print("summer")
elif number <= 11:
    print("autumn")





